package com.java.project;

public class Arrays {
	public static void main(String[] args) {
		
		int [] mark = {60,70,80,90} ;
		System.out.println("accen");
		
		for(int i1 = mark.length-1; i1>=0 ;i1-- ) {
			System.out.println(mark[i1]+"");
		}
		System.out.println("decc");
		for(int i2 = 0; i2<=mark.length;i2++) {
			System.out.println(mark[i2]+"");
			
		}
	}

}
